void log_alarm_Draw(uint8_t *MenuRender)

{
	if (RENDER_CHK(*MenuRender, REDRAW)) {
		log_alarm_lcd();
		log_alarm_items();
		log_alarm_Reneder(0x007F);
		RENDER_END(*MenuRender, REDRAW);
	}

	if (RENDER_CHK(Menu_Render, REFRESH0)) {
		log_alarm_Reneder(0x007F);
		RENDER_END(Menu_Render, REFRESH0);
	}
}
